-- 5. RLS Policies Definition

-- companies
DROP POLICY IF EXISTS "Companies access" ON public.companies;
CREATE POLICY "Companies access" ON public.companies
  FOR ALL USING (
    id = public.get_user_company_id()
  );

-- We allow direct insert for newly registered users on companies so they can bootstrap
DROP POLICY IF EXISTS "Companies creation" ON public.companies;
CREATE POLICY "Companies creation" ON public.companies
  FOR INSERT WITH CHECK (
    auth.uid() IS NOT NULL
  );

-- company_users & company_modules
DROP POLICY IF EXISTS "company_users access" ON public.company_users;
CREATE POLICY "company_users access" ON public.company_users
  FOR ALL USING (
    company_id = public.get_user_company_id()
  );

DROP POLICY IF EXISTS "company_modules access" ON public.company_modules;
CREATE POLICY "company_modules access" ON public.company_modules
  FOR ALL USING (
    company_id = public.get_user_company_id()
  );

-- Generic Rule for Business Tables:
-- The rule ensures maximum backward compatibility (legacy): 
-- If company_id IS NULL -> Everyone has access (legacy data without SaaS constraints)
-- If company_id IS NOT NULL -> Only the users whose get_user_company_id() matches have access.

DROP POLICY IF EXISTS "Sites isolation" ON public.sites;
CREATE POLICY "Sites isolation" ON public.sites
