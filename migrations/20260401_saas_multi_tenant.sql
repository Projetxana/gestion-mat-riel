-- 3. Backend Functions
-- Function to retrieve current security context company ID based on auth.uid()
CREATE OR REPLACE FUNCTION public.get_user_company_id()
RETURNS BIGINT
LANGUAGE sql
SECURITY DEFINER
AS $$
  SELECT cu.company_id
  FROM public.company_users cu
  JOIN public.users u ON u.id::text = cu.user_id
  WHERE u.auth_user_id = auth.uid()
  LIMIT 1;
$$;

-- Function to check if a specific module is active for user's company
CREATE OR REPLACE FUNCTION public.is_module_active(p_module_name TEXT)
RETURNS BOOLEAN
LANGUAGE sql
SECURITY DEFINER
AS $$
  SELECT COALESCE(
    (SELECT is_active 
     FROM public.company_modules 
     WHERE company_id = public.get_user_company_id() AND module_name = p_module_name),
    false
  );
$$;

-- Example: Function to accept an invitation
CREATE OR REPLACE FUNCTION public.accept_invite(p_token UUID, p_user_id TEXT)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_company_id BIGINT;
BEGIN
  -- Mark the intent as accepted and map the user if valid
  SELECT company_id INTO v_company_id 
  FROM public.invites 
  WHERE token = p_token AND status = 'pending';

  IF v_company_id IS NOT NULL THEN
    -- Join company
    INSERT INTO public.company_users (company_id, user_id, role)
    VALUES (v_company_id, p_user_id, 'user')
    ON CONFLICT DO NOTHING;

    -- Update invite status
    UPDATE public.invites SET status = 'accepted' WHERE token = p_token;
    RETURN true;
  END IF;

  RETURN false;
END;
$$;

-- Function to create a company and automatically add the creator as admin
CREATE OR REPLACE FUNCTION public.create_company(p_name TEXT, p_user_id TEXT)
RETURNS BIGINT
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_company_id BIGINT;
BEGIN
  -- Insert into companies
  INSERT INTO public.companies (name) VALUES (p_name) RETURNING id INTO v_company_id;
  
  -- Insert mapping
  INSERT INTO public.company_users (company_id, user_id, role)
  VALUES (v_company_id, p_user_id, 'admin');
  
  -- Optionally add a default user in 'users' if passing a new ID to ensure referential integrity
  -- (Since users might be generated later, wait, 'users' must exist first if there is an FK? 
  -- company_users DOES NOT have an FK to users, it just joins on user_id::text)

  RETURN v_company_id;
END;
$$;

